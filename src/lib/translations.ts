export function translateValue(value?: string) {
  if (!value) return value;
  const exact = exactTranslations[value.toLowerCase()];
  if (exact) return exact;
  return applyReplacements(value, phraseReplacements);
}

export function translateSpellEffect(value?: string) {
  if (!value) return "Efecto no especificado.";
  const exact = exactTranslations[value.toLowerCase()];
  if (exact) return exact;
  return applyReplacements(value, spellEffectReplacements);
}

export function translateList(values: string[]) {
  return values.map((value) => translateValue(value) || value);
}

function applyReplacements(value: string, replacements: Array<[RegExp, string]>) {
  return replacements.reduce((current, [pattern, replacement]) => current.replace(pattern, replacement), value);
}
